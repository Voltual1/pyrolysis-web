    @Composable get() {
        val customColors = ThemeManager.customColorSet
        return if (ThemeManager.isAppDarkTheme) {
            customColors?.darkSet?.messageLikeBg ?: message_like_bg_dark
        } else {
            customColors?.lightSet?.messageLikeBg ?: message_like_bg
        }
    }

